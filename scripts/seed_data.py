BASE = Path(__file__).resolve().parent.parent
KROOT = BASE / "data" / "knowledge"


def write_doc(tenant: str, filename: str, meta: dict, body: str):
    d = KROOT / tenant
    d.mkdir(parents=True, exist_ok=True)
    front = json.dumps(meta, ensure_ascii=False)
    content = f"---\n{front}\n---\n{body.strip()}\n"
    (d / filename).write_text(content, encoding="utf-8")
    print(f"  [{tenant}] {filename}  [{meta['doc_type']}/{meta['department']}]")


